<template>
  <BaseModalContainer
    ref="modal"
    :is-multiple="isMultiple"
    @show.once="handleCall"
  >
    <BasePaginatedSegmentContainer
      ref="segment"
      class="basic scrolling content"
      :class="{
        'full-height': responseData
      }"
      :response-data="responseData"
      :is-loading="isLoading"
      :error="error"
      :scope="scope"
      :text-scope="textScope"
      :limit="limit"
      :is-with-infinite-scroll="isWithInfiniteScroll"
      :scroll-context="scrollContext"
      @focus="handleFocus"
    >
      <template #top>
        <slot
          name="top"
        />
      </template>

      <template
        #default="slotProps"
      >
        <slot
          :[scope]="slotProps[scope]"
        />
      </template>
    </BasePaginatedSegmentContainer>
  </BaseModalContainer>
</template>

<script>
import {
  mapState
} from 'pinia'
import layoutStore from '@/stores/layout'
import BaseModalContainer
  from '@/components/containers/modals/BaseModalContainer.vue'
import BasePaginatedSegmentContainer
  from '@/components/containers/segments/BasePaginatedSegmentContainer.vue'

export default {
  name: 'BasePaginatedSegmentModalContainer',
  components: {
    BaseModalContainer,
    BasePaginatedSegmentContainer
  },
  props: {
    responseData: Object,
    isLoading: Boolean,
    error: Error,
    scope: String,
    textScope: String,
    limit: Number,
    isMultiple: Boolean
  },
  emits: [
    'call'
  ],
  data () {
    return {
      scrollContext: null
    }
  },
  computed: {
    ...mapState(
      layoutStore,
      [
        'isWithInfiniteScroll'
      ]
    )
  },
  methods: {
    handleCall () {
      this.$emit(
        'call'
      )

      this.scrollContext =
        this.$refs.segment.$el
    },
    handleFocus () {
      this.scrollToTop()
    },
    scrollToTop () {
      this.$refs
        .segment
        .scrollToTop()
    },
    show () {
      this.$refs
        .modal
        .show()
    },
    hide () {
      this.$refs
        .modal
        .hide()
    },
    refresh () {
      this.$refs
        .segment
        .refresh()
    }
  }
}
</script>

